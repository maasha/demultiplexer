# Module containing class methods for clearing and resetting a terminal screen.
module Screen
  # Method that uses console code to clear the screen.
  #
  # Returns nothing.
  def self.clear
    print "\e[H\e[2J"
  end

  # Method that uses console code to move cursor to 1,1 coordinate.
  #
  # Returns nothing.
  def self.reset
    print "\e[1;1H"
  end
end
